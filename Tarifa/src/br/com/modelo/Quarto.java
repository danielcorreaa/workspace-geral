package br.com.modelo;

public class Quarto {
	
	private int portaNumero;
	private Boolean vago;
	
	public Quarto() {
		super();
	}
	
	public int getPortaNumero() {
		return portaNumero;
	}
	public void setPortaNumero(int portaNumero) {
		this.portaNumero = portaNumero;
	}
	public Boolean getVago() {
		return vago;
	}
	public void setVago(Boolean vago) {
		this.vago = vago;
	}
	
	
}
